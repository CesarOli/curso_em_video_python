c = input("digite algo: ")

print('O tipo primitivo de algo é ', type(c))
print('É alfanumérico?' ,c.isalnum())
print('É letra do alfabeto? ',c.isalpha())
print('É um número decimal? ',c.isdecimal())
print('Está em letras minúsculas? ',c.islower())
print('Está em letras maiuscúlas? ',c.isupper)


